disponibilizados, além das outras sanções previstas em Lei. </p>

<h3>Direitos do usuário do Mapa Cultural de Belo Horizonte e responsabilidades de seus administradores </h3>

<p>A Secretaria Municipal de Cultura é responsável pela segurança das informações coletadas, armazenadas e processadas pelo 
Mapa Cultural de Belo Horizonte, bem como pela segurança das informações transmitidas aos serviços disponibilizados 
através do Mapa Cultural de Belo Horizonte.</p>

<p>A Secretaria Municipal de Cultura utilizará as medidas adequadas, de acordo com o estado da tecnologia, para garantir a 
segurança e integridade das informações pessoais sob sua custódia e evitar qualquer modalidade de acesso indevido. </p>

<p>Os acessos às informações pessoais dos usuários não poderão ser comercializados e somente poderão ser efetuados por 
servidores autorizados pela Secretaria Municipal de Cultura ou por ordem judicial. Os acessos às informações pessoais 
serão auditados e monitorados pela Secretaria Municipal de Cultura. </p>

<p>Com o intuito de assegurar a liberdade de expressão e impedir a censura, a Secretaria Municipal de Cultura segue os 
princípios e garantias do Marco Civil da Internet, Lei nº 12.965, de 23 de abril de 2014. </p>

<p>A Secretaria Municipal de Cultura poderá suspender a veiculação de informações e/ou dados por fundado receio 
referente à ilegalidade ou uso indevido de direitos de terceiros. </p>

<h3>Política de uso de dados pessoais </h3>

<p>O usuário do Mapa Cultural de Belo Horizonte será informado previamente sobre quais dados pessoais e 
obrigatórios serão repassados à Secretaria Municipal de Cultura. </p>

<p>As informações pessoais fornecidas pelo usuário e tratadas pelo Mapa Cultural de Belo Horizonte serão utilizadas 
para o fornecimento do serviço, com suas funcionalidades descritas neste Termo de Uso. </p>

<p>Estas informações poderão ser utilizadas pela Secretaria Municipal de Cultura ou transferidas a 
outros Órgãos do Governo, sempre que houver requerimento formal por parte destes e autorização do usuário 
e, por último, quando a legislação o autorizar. </p>

<h3>Condições gerais </h3>

<p>Ao utilizar o Mapa Cultural de Belo Horizonte, o usuário será informado previamente sobre quais informações pessoais 
serão de visualização pública e quais são privadas. Todas as informações públicas poderão ser visualizadas por qualquer 
cidadão, independentemente de estar cadastrado no Mapa Cultural de Belo Horizonte.</p> 

<p>As informações do usuário do Mapa Cultural de Belo Horizonte somente poderão ser modificadas através do sítio web do sistema, 
mediante o uso de suas informações de acesso. </p>

<p>Caso o usuário tenha perdido as suas informações de acesso, ele poderá utilizar o serviço de recuperação disponibilizado 
no site do Mapa Cultural de Belo Horizonte ou através de suporte da equipe técnica pelo e-mail mapacultural@pbh.gov.br.</p>

<h3>Suspensão ou término da disponibilização do Mapa Cultural de Belo Horizonte </h3>

<p>O Mapa Cultural de Belo Horizonte poderá ser suspenso ou ter sua disponibilização cessada a qualquer momento, mediante comunicação prévia, aos seus usuários. </p>

