<h3>Introdução </h3>

<p>Este Termo de Uso descreve as condições de uso do serviço Mapa Cultural de Belo Horizonte, incluindo os 
direitos e deveres de seus usuários e a política de uso de seus dados pessoais. </p>

<p>O serviço Mapa Cultural de Belo Horizonte é gratuito e sua utilização é uma faculdade do cidadão que, 
ao utilizá-lo, adere aos termos aqui dispostos, concordando e se submetendo integralmente às suas condições. </p>

<h3>Serviços disponíveis através do Mapa Cultural de Belo Horizonte </h3>

<p>Os serviços ofertados pela plataforma são os seguintes: cadastramento de espaços, agentes, eventos, 
projetos e oportunidades; consulta aos cadastros de espaços, de agentes, de eventos, de projetos e de 
oportunidades; apresentação dos dados cadastrais em interface de georreferenciamento; exportação das 
informações públicas existentes nos cadastros mantidos pelo sistema; execução, acompanhamento e 
publicação dos resultados de projetos e oportunidades.</p>

<p>Novos serviços poderão ser disponibilizados a qualquer momento e os serviços existentes poderão 
ser removidos mediante comunicação prévia aos seus usuários através do http://mapaculturalbh.pbh.gov.br/.</p>

<h3>Deveres do usuário do Mapa Cultural de Belo Horizonte</h3>

<p>A utilização do Mapa Cultural de Belo Horizonte somente pode se dar em caráter artístico e cultural, 
para propósitos lícitos e de modo a não afetar os direitos ou possibilidades de uso do sistema por terceiros. 
Não serão toleradas condutas ilícitas, imorais ou que possam molestar ou ser inconvenientes para os administradores 
do sistema, para os serviços disponibilizados ou para terceiros. Da mesma forma, o Mapa Cultural de Belo Horizonte 
não pode ser utilizado para difamar, abusar, assediar, perseguir, ameaçar ou violar quaisquer direitos individuais 
ou para a transmissão de conteúdo ilícito. </p>

<p>O usuário tem responsabilidade pessoal sobre suas informações de acesso ao Mapa Cultural de Belo Horizonte 
(login, senha, token, certificado digital, frase de acesso ou outros) e deverá mantê-las em segurança, 
estritamente sob seu controle pessoal, não franqueando, em nenhuma circunstância, seu acesso a terceiros. </p>

<p>O usuário deverá comunicar imediatamente ao suporte do Mapa Cultural de Belo Horizonte, através do 
e-mail mapacultural@pbh.gov.br acerca de qualquer suspeita de acesso indevido ao sistema, de falha de 
segurança ou do comprometimento de suas informações de acesso.</p>

<p>O usuário é responsável pela veracidade e pela atualização dos dados que informar ao Mapa Cultural de Belo Horizonte, 
bem como aos serviços disponíveis através dele. O fornecimento de informação falsa poderá causar 
resultados inexatos e indesejados em relação aos serviços. </p>

<p>Ao ser identificada publicação que viole, de alguma forma, este Termo de Uso, o conteúdo terá sua publicação 
suspensa pelos administradores. Em caso de reincidência, o acesso do usuário poderá ser bloqueado na plataforma.  
O usuário responsável será notificado pelo e-mail utilizado ao fazer login. Após enviada a notificação, o usuário 
terá três dias corridos para se manifestar. A justificativa será analisada e podem ser solicitadas mais informações 
para que seja deferida ou indeferida. Não sendo aceita ou caso não haja resposta à notificação, o conteúdo será 
excluído definitivamente após o vencimento do prazo.</p>

